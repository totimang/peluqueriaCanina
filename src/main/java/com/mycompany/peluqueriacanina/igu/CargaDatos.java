    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        String nombreMasco = txtNombre.getText();
        String raza = txtRaza.getText();
        String color = txtColor.getText();
        String observaciones = txtObservaciones.getText();
        String alergico = (String) cmbAlergico.getSelectedItem();
        String atenEsp = (String) cmbAtEsp.getSelectedItem();
        
        String nombreDuenio = txtNomDuenio.getText();
        String celDuenio = txtCelDuenio.getText();
        
        control.guardar(nombreMasco, raza, color, observaciones, alergico, atenEsp, 
                nombreDuenio, celDuenio);
        
        JOptionPane optionPane = new JOptionPane("Se guardo correctamente");//MENSAJE
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);         //TIPO DE MENSAJE
        JDialog dialog = optionPane.createDialog("Guardado Exitoso");         //TITULO DEL MENSAJE
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        
    }//GEN-LAST:event_btnGuardarActionPerformed


